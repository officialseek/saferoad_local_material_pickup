function toId(code) {
  return code.toLowerCase().replace(/[^a-z0-9]/g, '-')
}

const lines = [
  "import type { SignCategoryId } from './signCategories'",
  '',
  'export interface Vagmarke {',
  '  id: string',
  '  code: string',
  '  name: string',
  '  categoryId: SignCategoryId',
  '  unit: string',
  '}',
  '',
  '/** Vägmärken enligt Transportstyrelsen (TSFS 2019:74). */',
  'export const vagmarken: Vagmarke[] = [',
]

for (const [categoryId, signs] of Object.entries(catalog)) {
  for (const [code, name] of signs) {
    const id = toId(code)
    const escapedName = name.replace(/'/g, "\\'")
    lines.push(
      `  { id: '${id}', code: '${code}', name: '${escapedName}', categoryId: '${categoryId}', unit: 'st' },`,
    )
  }
}

lines.push(']', '')

const outputPath = join(__dirname, '..', 'src', 'data', 'vagmarken.ts')
writeFileSync(outputPath, lines.join('\n'), 'utf8')
console.log(`Generated ${Object.values(catalog).flat().length} vägmärken -> ${outputPath}`)
